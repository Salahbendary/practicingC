#include <stdio.h>

int main () {
	setvbuf(stdout, NULL, _IONBF, 0);
	setvbuf(stderr, NULL, _IONBF, 0);
	unsigned char u8isPerfectSquare = 0; /* input for Positive Integer*/
	unsigned char u8number = 1; /* Number to check if*/
	printf("Enter the number:\t");
	scanf("%d",&u8isPerfectSquare);
	while(u8number*u8number<u8isPerfectSquare) {
		u8number++; }

	if(u8number*u8number==u8isPerfectSquare) {
			printf("is perfect square");
		}
	else { printf("not perfect square");
	}
	return 0 ;
}
